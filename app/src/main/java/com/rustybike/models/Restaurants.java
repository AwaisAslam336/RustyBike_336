package com.rustybike.models;

public class Restaurants {
    private int RestaurantId;
    private String RestaurantName;
    private String RestaurantPhone;
    private String RestaurantPostCode;
    private String RestaurantAddress;
    private String RestaurantCity;
    private String RestaurantFax;
    private String RestaurantStatus;
    private String RestaurantLogo;

    public int getRestaurantId() {
        return RestaurantId;
    }

    public void setRestaurantId(int restaurantId) {
        RestaurantId = restaurantId;
    }

    public String getRestaurantName() {
        return RestaurantName;
    }

    public void setRestaurantName(String restaurantName) {
        RestaurantName = restaurantName;
    }

    public String getRestaurantPhone() {
        return RestaurantPhone;
    }

    public void setRestaurantPhone(String restaurantPhone) {
        RestaurantPhone = restaurantPhone;
    }

    public String getRestaurantPostCode() {
        return RestaurantPostCode;
    }

    public void setRestaurantPostCode(String restaurantPostCode) {
        RestaurantPostCode = restaurantPostCode;
    }

    public String getRestaurantAddress() {
        return RestaurantAddress;
    }

    public void setRestaurantAddress(String restaurantAddress) {
        RestaurantAddress = restaurantAddress;
    }

    public String getRestaurantCity() {
        return RestaurantCity;
    }

    public void setRestaurantCity(String restaurantCity) {
        RestaurantCity = restaurantCity;
    }

    public String getRestaurantFax() {
        return RestaurantFax;
    }

    public void setRestaurantFax(String restaurantFax) {
        RestaurantFax = restaurantFax;
    }

    public String getRestaurantStatus() {
        return RestaurantStatus;
    }

    public void setRestaurantStatus(String restaurantStatus) {
        RestaurantStatus = restaurantStatus;
    }

    public String getRestaurantLogo() {
        return RestaurantLogo;
    }

    public void setRestaurantLogo(String restaurantLogo) {
        RestaurantLogo = restaurantLogo;
    }
}
